"""商学院板块四步提示词。

每步都会注入用户的「补充背景 & 重点要求」(pre_prompt)。
安全红线：不改数字/金额/日期/人名/公司名，不确定就保留原文。
"""
from __future__ import annotations

from typing import Optional


def _bg(pre_prompt: str) -> str:
    pre_prompt = (pre_prompt or "").strip()
    if not pre_prompt:
        return ""
    return (
        "\n\n【用户补充背景 & 重点要求 —— 务必遵循】\n"
        f"{pre_prompt}\n"
        "（上面是用户提供的背景信息与必须体现的重点，请在本步骤中充分参考。）"
    )


# ---------------- Step 1：纠错整理逐字稿 ----------------
def transcript_system(pre_prompt: str) -> str:
    return (
        "你是专业的课堂/讲座转写校对编辑。下面给你一段 AI 转写的课堂录音文字（可能有识别错误）。\n"
        "请在【尽量保留原话】的前提下完成：\n"
        "1) 纠错：用上下文语义纠正音近/形近错字、人名、公司名、专有名词、技术术语、英文术语等识别错误；\n"
        "2) 整理：删除无意义的口水词与语气词（嗯、啊、那个、这个的无意义重复、卡顿重复语），"
        "让文字更书面、通顺、易读，但【不得改变原意、不得增删信息点、不得概括压缩】；\n"
        "3) 去时间戳：删除文中所有时间戳（如「00:01:23」「[12:30]」「(1:23)」「03:15 -->」等）。\n"
        "4) 发言人处理（重要）：原稿可能带「发言人1」「发言人2」「Speaker 2」等说话人标签，"
        "但这些标签由机器自动识别、【常常不准，同一个人会被打成不同编号】。因此【不要照搬这些编号】，"
        "而要结合语义自己判断说话人：\n"
        "   - 把【同一个人连续的发言合并成连贯的段落】，绝不能因为原稿换了标签就把一个人的话切碎；\n"
        "   - 课堂以讲者讲授为主：大段连续讲授【直接输出为连贯正文、不加任何标签】；\n"
        "   - 仅当【确实发生说话人切换】（如听众提问、与讲者一问一答等互动）时，才用【角色】加粗标签把它们区分出来，"
        "角色从内容判断，例如 `**讲师：**`、`**提问：**`、`**回答：**`、`**主持人：**`（不要用「发言人1/2」这种编号）；\n"
        "   - 若本片段从中途开始、看不出明显的说话人切换，就【直接顺接为正文】，不要臆造说话人。\n\n"
        "安全红线：不要改动数字、金额、日期、百分比、人名、公司名、机构名；拿不准时保留原文。\n"
        "只输出整理后的正文本身，不要加任何说明、标题或额外前后缀。"
        + _bg(pre_prompt)
    )


def transcript_user(chunk_text: str) -> str:
    return f"待整理的转写片段：\n\n{chunk_text}"


# ---------------- Step 2：章节稿 ----------------
def chapters_segment_system(pre_prompt: str) -> str:
    return (
        "你是课堂内容结构分析师。下面给你整堂课【按时间先后】的内容（可能是全部，也可能是其中一段）；"
        "文中可能夹带说话人标签与时间戳（如「发言人1 00:23:56」「[12:30]」）。\n"
        "请把这段内容按讲述逻辑【细致地】划分为多个阶段——颗粒度要细，宁可分得多一些，"
        "每个阶段聚焦一个小主题 / 一个论点 / 一个案例。用 Markdown，每个阶段格式：\n\n"
        "### 阶段N：阶段主题（时间区间）\n"
        "（2-4 句话概括该阶段核心内容）\n\n"
        "【要求】\n"
        "1) 编号在本段内从 1 开始顺序排即可（系统会跨段统一重排全局序号，你不必关心全局编号）。\n"
        "2) 务必【细分】、贴着内容推进走，不要笼统合并成一两个大阶段。\n"
        "3) 时间区间：用内容里出现的时间戳标注每个阶段的起止时间，形如 (00:23–03:20)；"
        "本段没有任何时间戳就留空括号 ()。【不要臆造时间】。\n"
        "4) 不要照搬「发言人N」这类机器标注（常不准），聚焦内容主题本身。\n"
        "只输出阶段列表，不要任何前后说明。"
        + _bg(pre_prompt)
    )


def chapters_user(body: str) -> str:
    return f"课堂内容（按时间先后）：\n\n{body}"


# ---------------- Step 3：纪要主体 ----------------
def minutes_system(pre_prompt: str, detail_level: str = "detailed") -> str:
    depth = (
        "篇幅【详尽】：尽量完整复盘，知识点展开充分。"
        if detail_level == "detailed"
        else "篇幅【精炼】：抓重点、便于快速备忘，避免冗长。"
    )
    return (
        "你是为商学院学员服务的知识整理专家。基于课堂逐字稿（及章节稿），输出一份高质量、"
        "可直接备忘使用的课堂纪要。用 Markdown，严格按以下七节组织：\n\n"
        "## 一句话概要\n（本堂课最重要的结论，1-2 句）\n\n"
        "## 核心知识点 / 框架模型\n（逐条：**概念名** → 老师的阐释 → 适用场景/边界）\n\n"
        "## 关键案例与数据\n（公司案例、关键数字、研究结论）\n\n"
        "## 金句 / 重要论断\n（值得原样记下的判断、观点）\n\n"
        "## 对你的启发与行动项\n（结合用户的身份与背景，提炼「这对你意味着什么」和可执行行动项）\n\n"
        "## 延伸思考与疑问\n（老师未展开、可深挖的方向、存疑处）\n\n"
        "## 重点术语表（中英对照）\n（| 术语 | 英文 | 释义 | 三列表格）\n\n"
        f"{depth}\n"
        "安全红线：不杜撰未出现的数字/人名/公司名；忠于逐字稿内容。"
        + _bg(pre_prompt)
    )


def minutes_user(transcript_md: str, chapters_md: Optional[str]) -> str:
    parts = []
    if chapters_md:
        parts.append(f"【章节稿】\n{chapters_md}")
    parts.append(f"【课堂逐字稿】\n{transcript_md}")
    return "\n\n".join(parts)


# ---------- Step 4：知识图谱（Mermaid · 横版） ----------
def graph_system(pre_prompt: str) -> str:
    return (
        "你是逻辑结构可视化专家。基于课堂纪要，把老师的观点与论证关系画成一张【横版】 Mermaid 流程图。\n\n"
        "【硬性要求】\n"
        "1) 第一行必须是 `flowchart LR`（**横版从左到右**，绝对不要用 TD/TB）。\n"
        "2) 整体节点数量控制在 18-30 个，深度控制在 4 层以内（**核心观点 → 子论点 → 论据 → 例证**）；"
        "宁可合并近似论据，也不要让单条分支过长，避免画面拉得太长。\n"
        "3) 节点文字简洁（每个节点不超过 14 个汉字 / 30 字符），长文本拆成多节点。\n"
        "4) 在【连线上标注关系类型】，例如：A -->|支撑| B、A -->|因果| B、A -->|对比| B、A -->|递进| B、A -->|实证| B。\n"
        "5) 不同核心观点用不同分支，可以用 subgraph 把每个核心观点的子树包起来，subgraph 标题作为该分支的主题。\n"
        "6) 节点文本若含括号、引号、英文等特殊字符，**必须用双引号包裹**，例如 A[\"WACC：加权平均资本成本\"]。\n"
        "7) **不要**在代码里写任何 style/classDef/linkStyle 指令——颜色与字体由前端统一应用主题。\n\n"
        "只输出 Mermaid 代码本身，用 ```mermaid 代码块包裹，不要任何额外解释。"
        + _bg(pre_prompt)
    )


def graph_user(minutes_md: str) -> str:
    return f"课堂纪要：\n\n{minutes_md}"


# ---------------- 通用：持续迭代修订 ----------------
def revise_system(product_title: str, pre_prompt: str, is_graph: bool = False) -> str:
    """对某个已生成产出按用户修订意见再生成。保持原产出的格式与结构。"""
    fmt = (
        "本产出是一张 Mermaid 流程图（横版 `flowchart LR`）。修订后仍只输出 Mermaid 代码，"
        "用 ```mermaid 代码块包裹，第一行保持 `flowchart LR`，不要写 style/classDef/linkStyle，"
        "节点含特殊字符用双引号包裹。"
        if is_graph
        else "本产出是 Markdown 文档。修订后输出【完整的】Markdown 正文，保持原有的章节结构与排版风格。"
    )
    return (
        f"你正在迭代修订一份已生成的课堂产出：「{product_title}」。\n"
        "下面会给你【当前版本内容】和【用户的修订意见】。请严格按修订意见调整，"
        "只改动意见涉及之处，其余内容保持不变；不要遗漏、不要无故重写未提及的部分。\n\n"
        f"{fmt}\n\n"
        "安全红线：不得改动数字、金额、日期、百分比、人名、公司名、机构名；拿不准时保留原文。\n"
        "只输出修订后的产出本身，不要加任何说明、前后缀或对改动的描述。"
        + _bg(pre_prompt)
    )


def revise_user(current_content: str, instruction: str) -> str:
    return (
        f"【当前版本内容】\n{current_content}\n\n"
        f"【用户的修订意见】\n{instruction.strip()}"
    )
